package point;

/**
 *
 * @author Maxime BLAISE
 */
public class Point {

    // Emplacement du point
    public int x, y;

    /**
     * Construit un point à partir des coordonnées en paramètre.
     *
     * @param x Abscisse
     * @param y Ordonnée
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
